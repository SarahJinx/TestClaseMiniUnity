using System;
using System.Threading;

class Program
{
    static void PlayNote(int frequency, int duration)
    {
        if (frequency > 0)
        {
            Console.Beep(frequency, duration);
        }
    }

    static void Main()
    {
        (int, int)[] notes = {
            (294, 250), (294, 250), (587, 500), (440, 500), (415, 500), (392, 500),
            (349, 500), (294, 250), (349, 250), (392, 250),
            (262, 250), (262, 250), (587, 500), (440, 500), (415, 500), (392, 500),
            (349, 500), (294, 250), (349, 250), (392, 250),
            (247, 500), // Low B
            
            // Extended main melody
            (294, 250), (349, 250), (392, 250), (440, 250), (349, 250), (294, 250),
            (262, 250), (294, 250), (349, 250), (392, 250), (440, 250), (349, 250),
            (294, 250), (262, 250), (247, 250), (262, 250), (294, 250), (349, 250),
            (392, 250), (440, 250), (349, 250), (294, 250), (262, 250), (247, 250),
            (220, 250), (247, 250), (262, 250), (294, 250), (349, 250), (392, 250),
            (440, 250), (349, 250), (294, 250), (262, 250), (247, 250), (220, 250)
        };

        foreach (var note in notes)
        {
            PlayNote(note.Item1, note.Item2);
        }
    }
}